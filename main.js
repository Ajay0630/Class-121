function setup() {
  canvas = createCanvas(800, 600);
  canvas.center();
  video = createCapture(VIDEO);
  video.hide();
  classifier = ml5.imageClassifier('MobileNet', modelLoaded);
}
function modelLoaded(){
  console.log("MobileNet is loaded and ready to go.");
}
function draw(){
  image(video, 0, 0, 800, 600);
  classifier.classify(video, gotResult);
}
var previous_result = "";
function gotResult(error, results) {
  if (error) {
    console.error(error);
  } else if ((results[0].confidence > 0.5) && (previous_result != results[0].label)) {
    console.log(results);
    previous_result = results[0].label;
    var synth = window.speechSynthesis;
    speak_data = "The object is " + results[0].label;
    var utterThis = new SpeechSynthesisUtterance(speak_data);
    synth.speak(utterThis);
    document.getElementById("name").innerHTML = results[0].label;
    document.getElementById("confidenceenceenceenceenceence").innerHTML = (results[0].confidence * 100).toFixed(1) + "%";
  }
}










































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































